package com.learn.javacore._9OOPS._02Inheritance;

public class Animal {
    private String name;
    private int age;

    public void makeSound(){}

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
